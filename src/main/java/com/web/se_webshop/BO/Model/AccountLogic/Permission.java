package com.web.se_webshop.BO.Model.AccountLogic;

/**
 * This enum represents different levels of permissions that can be assigned to users.
 * - USER: Standard user with basic access rights.
 * - STAFF: Staff member with additional access rights.
 * - ADMIN: Administrator with full access rights.
 */
public enum Permission {
    USER, STAFF, ADMIN
}
